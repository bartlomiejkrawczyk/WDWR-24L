def cdf(x: List[float], *args, **kwargs):
    x, y = sorted(x), list(np.arange(1, len(x)+1) / len(x))
    x.insert(0, 9000)
    y.insert(0, 0)
    return plt.step(x, y, where='post', *args, **kwargs)


